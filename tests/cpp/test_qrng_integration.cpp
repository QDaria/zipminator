TEST(mock_qrng_determinism) {
    // Two mocks with same seed should produce same output
    uint64_t seed = 0x123456789ABCDEF0ULL;

    qrng::MockQRNG mock1, mock2;
    mock1.reseed(seed);
    mock2.reseed(seed);

    mock1.initialize();
    mock2.initialize();

    uint8_t buffer1[256], buffer2[256];
    mock1.get_random_bytes(buffer1, sizeof(buffer1));
    mock2.get_random_bytes(buffer2, sizeof(buffer2));

    ASSERT(memcmp(buffer1, buffer2, sizeof(buffer1)) == 0,
           "Same seed should produce identical output");

    mock1.disconnect();
    mock2.disconnect();
}

