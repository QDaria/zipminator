// Test 4: Statistical Randomness Tests (Simplified NIST SP 800-22)
// ============================================================================

TEST(statistical_frequency_test) {
    qrng::MockQRNG mock;
    mock.initialize();

    // Generate test data
    const size_t test_size = 10000;
    uint8_t data[test_size];
    mock.get_random_bytes(data, test_size);

    // Count bit frequencies
    size_t ones = 0, zeros = 0;

    for (size_t i = 0; i < test_size; i++) {
        for (int bit = 0; bit < 8; bit++) {
            if ((data[i] >> bit) & 1) {
                ones++;
            } else {
                zeros++;
            }
        }
    }

    // Should be roughly 50/50 (within 5% tolerance)
    size_t total_bits = test_size * 8;
    double ones_ratio = (double)ones / total_bits;

    ASSERT(ones_ratio > 0.45 && ones_ratio < 0.55,
           "Bit frequency should be balanced");

    mock.disconnect();
}

TEST(statistical_runs_test) {
    qrng::MockQRNG mock;
    mock.initialize();

    const size_t test_size = 1000;
    uint8_t data[test_size];
    mock.get_random_bytes(data, test_size);

    // Count runs of identical bytes
    size_t max_run = 1;
    size_t current_run = 1;

    for (size_t i = 1; i < test_size; i++) {
        if (data[i] == data[i-1]) {
            current_run++;
            if (current_run > max_run) {
                max_run = current_run;
            }
        } else {
            current_run = 1;
        }
    }

    // Maximum run should not be excessive (< 10 for 1000 bytes)
    ASSERT(max_run < 10, "Maximum run length should be reasonable");

    mock.disconnect();
}

TEST(statistical_uniqueness_test) {
    qrng::MockQRNG mock;
    mock.initialize();

    const size_t test_size = 256;
    uint8_t data[test_size];
    mock.get_random_bytes(data, test_size);

    // Count unique bytes
    std::set<uint8_t> unique_bytes(data, data + test_size);

    // Should see significant variety (at least 200/256 unique)
    ASSERT(unique_bytes.size() >= 200,
           "Should have good byte diversity");

    mock.disconnect();
}

