TEST(entropy_pool_concurrent_access) {
    auto mock = std::make_unique<qrng::MockQRNG>();
    qrng::EntropyPool pool(std::move(mock));

    pool.initialize();

    // Simulate multiple consumers
    const int num_threads = 4;
    const int bytes_per_thread = 1024;
    std::vector<std::thread> threads;
    std::atomic<int> success_count(0);

    for (int i = 0; i < num_threads; i++) {
        threads.emplace_back([&pool, &success_count, bytes_per_thread]() {
            uint8_t buffer[bytes_per_thread];
            if (pool.get_random_bytes(buffer, sizeof(buffer)) == qrng::QRNGStatus::OK) {
                success_count++;
            }
        });
    }

    for (auto& thread : threads) {
        thread.join();
    }

    ASSERT_EQ(success_count.load(), num_threads, "All threads should succeed");

    pool.shutdown();
}

