void run_all_tests() {
    std::cout << "\n========================================" << std::endl;
    std::cout << "QRNG Integration Test Suite" << std::endl;
    std::cout << "========================================\n" << std::endl;

    // Run all tests
    run_test_mock_qrng_initialization();
    run_test_mock_qrng_random_generation();
    run_test_mock_qrng_determinism();
    run_test_mock_qrng_health_check();

    run_test_entropy_pool_initialization();
    run_test_entropy_pool_random_generation();
    run_test_entropy_pool_concurrent_access();
    run_test_entropy_pool_statistics();

    run_test_kyber_qrng_keypair_generation();
    run_test_kyber_qrng_full_kem_cycle();
    run_test_kyber_qrng_deterministic_with_seed();

    run_test_statistical_frequency_test();
    run_test_statistical_runs_test();
    run_test_statistical_uniqueness_test();

    run_test_performance_mock_qrng_throughput();
    run_test_performance_entropy_pool_latency();

    run_test_error_handling_uninitialized_pool();
    run_test_error_handling_null_buffer();

    // Print results
    std::cout << "\n========================================" << std::endl;
    std::cout << "Test Results" << std::endl;
    std::cout << "========================================\n" << std::endl;

    int passed = 0, failed = 0;
    double total_time = 0;

    for (const auto& result : test_results) {
        std::string status = result.passed ? "\033[32mPASS\033[0m" : "\033[31mFAIL\033[0m";
        std::cout << "[" << status << "] " << std::left << std::setw(45)
                  << result.name << " (" << std::fixed << std::setprecision(2)
                  << result.duration_ms << " ms)" << std::endl;

        if (!result.passed) {
            std::cout << "       Error: " << result.error_message << std::endl;
            failed++;
        } else {
            passed++;
        }

        total_time += result.duration_ms;
    }

    std::cout << "\n========================================" << std::endl;
    std::cout << "Summary: " << passed << " passed, " << failed << " failed" << std::endl;
    std::cout << "Total time: " << total_time << " ms" << std::endl;
    std::cout << "========================================\n" << std::endl;
}

int main() {
    run_all_tests();

    // Return non-zero if any tests failed
    for (const auto& result : test_results) {
        if (!result.passed) {
            return 1;
        }
    }

    return 0;
}
