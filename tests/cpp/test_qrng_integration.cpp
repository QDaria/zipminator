TEST(kyber_qrng_keypair_generation) {
    // Initialize global entropy pool with mock
    auto mock = std::make_unique<qrng::MockQRNG>();
    qrng::GlobalEntropyPool::initialize(std::move(mock));

    uint8_t pk[kyber768::KYBER_PUBLICKEYBYTES];
    uint8_t sk[kyber768::KYBER_SECRETKEYBYTES];

    int result = kyber768::crypto_kem_keypair_qrng(pk, sk);
    ASSERT_EQ(result, 0, "Keypair generation should succeed");

    // Verify keys are not all zeros
    bool pk_nonzero = false, sk_nonzero = false;

    for (size_t i = 0; i < sizeof(pk); i++) {
        if (pk[i] != 0) pk_nonzero = true;
    }

    for (size_t i = 0; i < sizeof(sk); i++) {
        if (sk[i] != 0) sk_nonzero = true;
    }

    ASSERT(pk_nonzero, "Public key should not be all zeros");
    ASSERT(sk_nonzero, "Secret key should not be all zeros");

    qrng::GlobalEntropyPool::shutdown();
}

TEST(kyber_qrng_full_kem_cycle) {
    // Initialize global entropy pool
    auto mock = std::make_unique<qrng::MockQRNG>();
    qrng::GlobalEntropyPool::initialize(std::move(mock));

    // Generate keypair
    uint8_t pk[kyber768::KYBER_PUBLICKEYBYTES];
    uint8_t sk[kyber768::KYBER_SECRETKEYBYTES];
    ASSERT_EQ(kyber768::crypto_kem_keypair_qrng(pk, sk), 0,
              "Keypair generation failed");

    // Encapsulate
    uint8_t ct[kyber768::KYBER_CIPHERTEXTBYTES];
    uint8_t ss_enc[kyber768::KYBER_SHAREDSECRETBYTES];
    ASSERT_EQ(kyber768::crypto_kem_enc_qrng(ct, ss_enc, pk), 0,
              "Encapsulation failed");

    // Decapsulate
    uint8_t ss_dec[kyber768::KYBER_SHAREDSECRETBYTES];
    ASSERT_EQ(kyber768::crypto_kem_dec_qrng(ss_dec, ct, sk), 0,
              "Decapsulation failed");

    // Verify shared secrets match
    ASSERT(memcmp(ss_enc, ss_dec, kyber768::KYBER_SHAREDSECRETBYTES) == 0,
           "Shared secrets should match");

    qrng::GlobalEntropyPool::shutdown();
}

TEST(kyber_qrng_deterministic_with_seed) {
    // Two runs with same seed should produce same keypairs
    uint64_t seed = 0xDEADBEEFCAFEBABEULL;

    uint8_t pk1[kyber768::KYBER_PUBLICKEYBYTES];
    uint8_t sk1[kyber768::KYBER_SECRETKEYBYTES];

    uint8_t pk2[kyber768::KYBER_PUBLICKEYBYTES];
    uint8_t sk2[kyber768::KYBER_SECRETKEYBYTES];

    // First run
    {
        auto mock = qrng::create_mock_qrng(seed);
        qrng::GlobalEntropyPool::initialize(std::move(mock));
        kyber768::crypto_kem_keypair_qrng(pk1, sk1);
        qrng::GlobalEntropyPool::shutdown();
    }

    // Second run with same seed
    {
        auto mock = qrng::create_mock_qrng(seed);
        qrng::GlobalEntropyPool::initialize(std::move(mock));
        kyber768::crypto_kem_keypair_qrng(pk2, sk2);
        qrng::GlobalEntropyPool::shutdown();
    }

    ASSERT(memcmp(pk1, pk2, sizeof(pk1)) == 0, "Public keys should match");
    ASSERT(memcmp(sk1, sk2, sizeof(sk1)) == 0, "Secret keys should match");
}

