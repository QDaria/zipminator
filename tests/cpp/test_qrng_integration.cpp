TEST(performance_mock_qrng_throughput) {
    qrng::MockQRNG mock;
    mock.initialize();

    const size_t test_size = 1024 * 1024;  // 1 MB
    std::vector<uint8_t> buffer(test_size);

    auto start = std::chrono::high_resolution_clock::now();
    mock.get_random_bytes(buffer.data(), test_size);
    auto end = std::chrono::high_resolution_clock::now();

    double duration_ms = std::chrono::duration<double, std::milli>(end - start).count();
    double throughput_mbps = (test_size / (1024.0 * 1024.0)) / (duration_ms / 1000.0);

    std::cout << "  Mock QRNG Throughput: " << throughput_mbps << " MB/s" << std::endl;

    // Should be reasonably fast (> 10 MB/s)
    ASSERT(throughput_mbps > 10.0, "Throughput should be reasonable");

    mock.disconnect();
}

TEST(performance_entropy_pool_latency) {
    auto mock = std::make_unique<qrng::MockQRNG>();
    qrng::EntropyPool pool(std::move(mock));
    pool.initialize();

    // Measure latency for small requests (already buffered)
    const int iterations = 1000;
    uint8_t buffer[32];

    auto start = std::chrono::high_resolution_clock::now();
    for (int i = 0; i < iterations; i++) {
        pool.get_random_bytes(buffer, sizeof(buffer));
    }
    auto end = std::chrono::high_resolution_clock::now();

    double total_us = std::chrono::duration<double, std::micro>(end - start).count();
    double avg_latency_us = total_us / iterations;

    std::cout << "  Entropy Pool Avg Latency: " << avg_latency_us << " μs" << std::endl;

    // Should be very fast when buffered (< 10 μs)
    ASSERT(avg_latency_us < 10.0, "Buffered access should be fast");

    pool.shutdown();
}

